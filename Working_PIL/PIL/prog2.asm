.model small
.stack
.data
	STRING1 DB "HELLO$"
	STRING2 DB "WORLD$"
	STR1 DB 32 DUP("$")
	STR2 DB 32 DUP("$")
	STR3 DB 64 DUP("$")
	STRLEN1 DB ?
	STRLEN2 DB ?
	O1 DB "EQUAL$"
	O2 DB "NOT EQUAL$"

.code

	MOV AX,@DATA
	MOV DS,AX

	;-----INPUT STRING-----

	MOV AH,0AH
	LEA DX,STR1
	INT 21H

	MOV AH,02H			;NEWLINE
	MOV DL,0AH
	INT 21H

	MOV AH,0AH
	LEA DX,STR2
	INT 21H

	MOV AH,02H			;NEWLINE
	MOV DL,0AH
	INT 21H

	;-----STRING LENGTH-----
	
