	LEA SI, STR1
	INC SI
	MOV CX, 0H
	MOV CL, BYTE PTR[SI]
	MOV BYTE PTR[STRLEN1],CL
	ADD CL,30H
	MOV AH,02H
	MOV DL,CL
	INT 21H

	;--------------------

	;-----STRING LENGTH STR2-----
	
	LEA SI, STR2
	INC SI
	MOV CX, 0H
	MOV CL, BYTE PTR[SI]
	MOV BYTE PTR[STRLEN2],CL
	ADD CL,30H
	MOV AH,02H
	MOV DL,CL
	INT 21H

	;--------------------

	;-----STRING REVERSE-----

	MOV CL,BYTE PTR[STRLEN1]
	MOV CH,0H

	LEA SI,STR1+2
	LEA DI,STR3

	DEC CX
	ADD DI,CX			;TO POINT TO LAST LETTER OF STRING
	INC CX

	L3:
	MOV DL,[SI]
	MOV [DI],DL
	DEC DI
	INC SI
	DEC CX
	JNZ L3

	MOV AH,02H			;NEWLINE
	MOV DL,0AH
	INT 21H

	MOV AH,09H			;DISPLAY STRING
	LEA DX,STR3
	INT 21H

	;--------------------

	;-----STRING CONCAT-----
	
	LEA DI,STR3+2

	LEA SI,STR1+2

	L4:
	CMP BYTE PTR[SI],"$"
	JE OUTL4
	MOV DL,[SI]
	MOV [DI],DL
	INC DI
	INC SI
	JNE L4
	OUTL4:

	LEA SI,STR2+2

	DEC DI				

	L5:
	CMP BYTE PTR[SI],"$"
	JE OUTL5
	MOV DL,[SI]
	MOV [DI],DL
	INC DI
	INC SI
	JNE L5
	OUTL5:

	MOV DL,"$"
	MOV BYTE PTR [DI], DL

	MOV AH,02H
	MOV DL,0AH
	INT 21H

	MOV AH,09H
	LEA DX,STR3+2
	INT 21H

	MOV AH,02H
	MOV DL,0AH
	INT 21H

	;--------------------

	;-----STRING COMPARE-----
	
	LEA SI,STR1+1
	LEA DI,STR2+2
	MOV CL, BYTE PTR[SI]
	MOV CH, 0H
	INC SI

	L10:
	CMP CL,0H
	JE L11
	MOV DL,BYTE PTR[SI]
	MOV DH,BYTE PTR[DI]
	CMP DH,DL
	JNE L12
	INC DI
	INC SI
	DEC CL
	CMP CL,0H
	JNE L10			

	L11:
	MOV AH,09H			;STRING PRINT
	LEA DX,O1
	INT 21H
	JMP L14

	L12:
	MOV AH,09H
	LEA DX,O2
	INT 21H

	L14:

	MOV AH,02H
	MOV DL,0AH
	INT 21H

	;--------------------

	MOV AH,04CH
	INT 21H
