.model small
.stack
.data
	string db "HELLOWORLD$"
	STR1 DB 16 DUP("$")
	STR2 DB 16 DUP("$")
.code
	MOV AX,@DATA
	MOV DS,AX

	MOV AH,09H			;STRING DISPLAY
	MOV DX,OFFSET string
	INT 21H

	MOV AH,02H			;NEWLINE
	MOV DL,0AH
	INT 21H

	MOV AH,09H			;STRING DISPLAY
	LEA DX,string
	INT 21H

	MOV AH,0AH			;KEYBOARD INPUT
	LEA DX,STR1
	INT 21H

	MOV AH,09H			;STRING DISPLAY
	LEA DX,STR1+2
	INT 21H

	MOV AH,02H			;NEWLINE
	MOV DL,0AH
	INT 21H

	MOV AH,0AH			;KEYBOARD INPUT
	LEA DX,STR1
	INT 21H

	MOV AH,09H			;STRING DISPLAY
	LEA DX,STR1+2
	INT 21H

	MOV AH,04CH
	INT 21H
END